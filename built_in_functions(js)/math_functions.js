//Math.max() : returns the maximum value
console.log(Math.max(20,30,5));

//Math.min() : returns the minimum value
console.log(Math.min(20,30,5));

//Math.round() : rounds a number to a nearest integer 
console.log(Math.round(6.8));

//Math.floor() : returns the largest integer less than or equal to given number 
console.log(Math.floor(6.8));

//Math.ceil() : returns the smallest integer greater than or euql to a given number 
console.log(Math.ceil(6.8));

//Math.random() : returns random number between 0 and 1
console.log(Math.random());